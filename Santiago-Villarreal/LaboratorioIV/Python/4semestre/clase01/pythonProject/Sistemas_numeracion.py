
# Sistema decimal
a = 10
print(f'a decimal: {a}')

# sistema binario
a = 0b1010
print(f'a binario: {a}')

# Sistema octal
a = 0o12
print(f'a octal: {a}')

# Sistema hexadecimal
a = 0xA
print(f'a hexadecimal: {a}')

# base decimal
a = int('23', 10)
print(f'a base decimal: {a}')

# base binario
a = int('10111', 2)
print(f'a base binaria: {a}')

# base octal
a = int('27', 8)
print(f'a base octal: {a}')

# base hexadecimal
a = int('17', 16)
print(f'a base hexadecimal: {a}')

# base 5
a = int('34', 5)
print(f'a base 5: {a}')
